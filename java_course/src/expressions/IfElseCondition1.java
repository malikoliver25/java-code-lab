package expressions;

public class IfElseCondition1 {

    public static void main(String[] args) {
        int num = 10;
        if(num >= 0 && num <= 10){
            System.out.println("The number is greater than or equal to 10.");
        }
        else{
            System.out.println("The number is less than or equal to 10.");
        }
    }
}
